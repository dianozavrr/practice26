import pandas as pd

df = pd.read_csv("students1.8.csv")

df['Середній_бал'] = df[['Математика', 'Фізика', 'Хімія', 'Інформатика', 'Англійська']].mean(axis=1).round(2)

print("Таблиця студентів з середнім балом:")
print(df)

середні_дисципліни = df[['Математика', 'Фізика', 'Хімія', 'Інформатика', 'Англійська']].mean().round(2)
print("\nСередній бал групи з кожної дисципліни:")
print(середні_дисципліни)
